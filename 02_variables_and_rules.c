#include <stdio.h>

int main() {
    int age = 20;     // Variable name should start with a letter and no special characters allowed
    float height = 5.9; // Declaring a float type variable
    char grade = 'A';  // Declaring a character type variable

    printf("Age: %d\n", age);
    printf("Height: %.1f\n", height);
    printf("Grade: %c\n", grade);

    return 0;
}

/* Sample Output:
Age: 20
Height: 5.9
Grade: A
*/
