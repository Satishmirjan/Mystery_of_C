#include <stdio.h>

int main() {
    int integer = 10;
    float decimal = 3.14;
    char character = 'C';

    printf("Integer: %d\n", integer);
    printf("Float: %.2f\n", decimal);
    printf("Character: %c\n", character);

    return 0;
}

/* Sample Output:
Integer: 10
Float: 3.14
Character: C
*/
